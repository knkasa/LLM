import os
import dspy
import crewai
from crewai import Agent, Task, Crew, LLM
from dspy.teleprompt import BootstrapFewShot

lm = dspy.LM(
    model="ollama_chat/llama3:8b",
    api_base="http://localhost:11434",
    api_key=""
)

dspy.configure(lm=lm)

llm = LLM(
    model="ollama_chat/llama3:8b",
    api_base="http://localhost:11434",
    api_key=""
)

# Define Email text block template.
class EmailDraft(dspy.Signature):
    """Generate professional business email."""
    # InputField is supposed to be provided in user query.  OutputField will be generated by LLM.
    intent = dspy.InputField(desc="Purpose of the email")
    email_context = dspy.InputField(desc="Background information")
    tone = dspy.InputField(desc="Tone of the email")
    recipient_context = dspy.InputField(desc="Who the recipient is")

    subject = dspy.OutputField(desc="Email subject")
    body = dspy.OutputField(desc="Full email body")

class EmailWriterModule(dspy.Module):
    def __init__(self):
        super().__init__()
        self.generate = dspy.Predict(EmailDraft)

    def forward(self, intent, email_context, tone, recipient_context):
        return self.generate(
            intent=intent,
            email_context=email_context,
            tone=tone,
            recipient_context=recipient_context
        )

