# Define custom evalueation methods.
def email_quality_metric(example, prediction):
    if prediction.subject is None or prediction.body is None:
        return False

    score = 0
    if len(prediction.subject) > 5:
        score += 1
    if len(prediction.body) > 50:
        score += 1
    if "dear" in prediction.body.lower():
        score += 1
    if "regards" in prediction.body.lower() or "sincerely" in prediction.body.lower():
        score += 1
    return score >= 3


# Train LLM with this logic.  You can save this logic in local for later use.
basic_email_module = EmailWriterModule() 
OPTIMIZED_MODULE_FILE = "optimized_email_module.json"

def optimize_email_module():
    teleprompter = BootstrapFewShot(
        metric=email_quality_metric,
        max_bootstrapped_demos=2,
        max_labeled_demos=2,
        max_rounds=1
    )

    print("\n🚀 Optimizing prompt with BootstrapFewShot")

    optimized_module = teleprompter.compile(
        basic_email_module,
        trainset=trainset
    )

    optimized_module.save(OPTIMIZED_MODULE_FILE)

    print("✅ Optimization complete")
    print("💾 Saved optimized module")

    return optimized_module

if os.path.exists(OPTIMIZED_MODULE_FILE):
    print("📦 Loading optimized module")
    optimized_email_module = EmailWriterModule()
    optimized_email_module.load(OPTIMIZED_MODULE_FILE)
else:
    optimized_email_module = optimize_email_module()


def create_patched_llm_call(module):
    def patched_call(self, prompt, **kwargs):
        print("⚡ Using DSPy EmailWriterModule instead of the LLM")

        result = module(
            intent="Follow up after product demo",
            email_context=prompt,
            tone="Professional",
            recipient_context="Business client"
        )
        
        print("📧 Generated Email Subject:", result.subject)
        print("📧 Generated Email Body:", result.body)

        return f"Subject: {result.subject}\n\n{result.body}"
    return patched_call

_original_llm_call = crewai.llm.LLM.call
def apply_monkey_patch(use_optimization=True):
    if use_optimization:
        print("\n🔧 APPLYING DSPy OPTIMIZATION")
        crewai.llm.LLM.call = create_patched_llm_call(optimized_email_module)
    else:
        print("\n⚠️ BASELINE MODE")
        crewai.llm.LLM.call = _original_llm_call

#=============== Finally, run the agent. ==========================

# If set True, it will use the DSPy logic.
apply_monkey_patch(True)

email_agent = Agent(
    role="Email Assistant",
    goal="Write professional business emails",
    backstory="Expert business communication specialist",
    llm = llm,
    verbose=True
)

task = Task(
    description="Write a follow-up email after a product demo discussing Salesforce integration.  The client name is Jason.",
    expected_output="A professional email with subject and body.",
    agent=email_agent
)

crew = Crew(
    agents=[email_agent],
    tasks=[task],
    verbose=True
)

result = crew.kickoff()

print("\nFINAL RESULT\n")
print(result)
